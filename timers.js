//timers ue estan incluidos en node.js 

//setInterval .......... Ejecuta un codigo cada cierto tiempo 

//setTimeout ........... Ejecuta codigo despues de que pase un tiempo 

setInterval(() => {
    console.log('Hello World')
}, 2000 )  // el codigo de hola mundo se repetira cada 2 segundos 


setTimeout (() => {
    console.log('Hello World')
}, 5000) // el codigo se compila despues de 5 segundos y se detiene 